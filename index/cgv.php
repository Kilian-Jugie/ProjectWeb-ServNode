            <h3>3.2</h3>
            <p>Vous pouvez sélectionner des produits dans la boutique en ligne, les déposer dans un panier virtuel en cliquant sur le bouton « Ajouter au panier » et qui pourra, à tout moment, être consulté dans la boutique en ligne en cliquant sur le symbole du panier.</p>

            <h3>3.3</h3>
            <p>Le clic sur le symbole du panier vous permet de lancer le processus de commande effectif. Vous pouvez effectuer le processus de commande avec ou sans création d'un compte d'utilisateur personnel. Durant le processus de commande, vous indiquez votre adresse et sélectionnez le mode d'expédition et de paiement (dans la mesure où plusieurs options sont disponibles). Pour certaines méthodes de paiement, vous devrez saisir d'autres données pour effectuer le paiement (p. ex. en cas de paiement par carte de crédit, le numéro de la carte, la date d'échéance et le code de sécurité). Si vous créez un compte d'utilisateur, vos données d'adresse seront conservées pour des commandes futures. Après la saisie des données précitées, vous accédez à une page (ci-après dénommée « Récapitulatif de la commande ») qui présente une nouvelle fois l'état de votre commande et qui vous permet d'identifier d'éventuelles erreurs de saisie. En cliquant sur la fonction « Retour » de votre navigateur Internet ou mobile, et « Modification » des champs concernés sur les pages qui s'affichent, vous pourrez alors modifier et corriger les produits sélectionnés et les informations qui les concernent avant de terminer votre commande et revenir au récapitulatif de votre commande via les boutons correspondants des différentes pages.</p>

            <h3>3.4</h3>
