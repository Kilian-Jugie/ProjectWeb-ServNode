            <p>Le prix des produits sélectionnés au titre de votre commande, taxe sur le chiffre d'affaires incluse, ainsi que les éventuels frais d'envoi à votre charge et leur montant, vous seront indiqués une nouvelle fois sous forme résumée dans votre récapitulatif de commande.</p>

