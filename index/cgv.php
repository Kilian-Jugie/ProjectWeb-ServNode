            <p>Le texte du contrat (comportant les éléments suivants: votre commande, les présentes CGV ainsi que les confirmations d'expédition que nous vous avons envoyées) sera enregistré par nos soins après la conclusion du contrat. Le texte du contrat sera mis à votre disposition par e-mail détaillant les différents éléments.</p>

